def resta(a,b):
    print(a-b)
    return a-b

def suma(a,b):
    print(a+b)
    return a+b

def multiplicacion(a,b):
    print(a*b)
    return a*b

def division(a,b):
    print(a/b)
    return a/b

def exponencial(a,b):
    print(a**b)
    return a**b

resul_resta = (resta(10,5))
print(resul_resta)

resul_suma = (suma(10,5))
print(resul_suma)

resul_multiplicacion = (multiplicacion(10,5))
print(resul_multiplicacion)

resul_division = (division(10,5))
print(resul_division)

resul_exponencial = (exponencial(10,5))
print(resul_exponencial)

